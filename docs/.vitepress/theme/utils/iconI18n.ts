export function localizeIconName(name: string, zhName?: string) {
  if (zhName) return zhName;
  if (iconNameMap[name]) return iconNameMap[name];

  const localizedParts = name.split('-').map((part) => iconNamePartMap[part] ?? part);
  return localizedParts.join('');
}

export function localizeIconTags(tags: string[] = [], zhTags?: string[]) {
  if (zhTags?.length) return zhTags;

  const localizedTags = tags
    .map((tag) => tagMap[tag])
    .filter((tag): tag is string => Boolean(tag));

  return localizedTags.length ? localizedTags : tags;
}

export function localizeIconCategories(categories: string[] = [], zhCategories?: string[]) {
  return zhCategories?.length ? zhCategories : categories.map(localizeCategoryName);
}
